class Solution:
    def addToArrayForm(self, num: List[int], k: int) -> List[int]:
        
        
        n = " "
        
        for i in num:
            n = n+ str(i)
            
        new=int(n)
        sum=new+k
        return str(sum)
            
            
        
             
        
            
            
            
        
            
        